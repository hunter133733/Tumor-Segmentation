import os, shutil, re

data_root = "/content/data"
target_imagesTr = "/content/nnUNet/nnUNet_raw/Dataset501/imagesTr"

for case_folder in os.listdir(data_root):
    case_path = os.path.join(data_root, case_folder)
    if not os.path.isdir(case_path):
        continue

    case_id = case_folder

    for file in os.listdir(case_path):
        if "flair.nii.gz" in file.lower():
            channel = "0000"
        elif "t1.nii.gz" in file.lower() and "t1gd" not in file.lower():
            channel = "0001"
        elif "t1gd.nii.gz" in file.lower() or "t1gd" in file.lower():
            channel = "0002"
        elif "t2.nii.gz" in file.lower():
            channel = "0003"
        else:
            continue

        src = os.path.join(case_path, file)
        dest = os.path.join(target_imagesTr, f"{case_id}_{channel}.nii.gz")

        shutil.copy(src, dest)
        print("Copied:", dest)

