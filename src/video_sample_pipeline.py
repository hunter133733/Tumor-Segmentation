# Binarizes files for easy sample training

import os
import nibabel as nib
import numpy as np

labels_dir = "/content/nnUNet/nnUNet_raw/Dataset501/labelsTr"

for file in os.listdir(labels_dir):
    if not file.endswith(".nii.gz"):
        continue

    path = os.path.join(labels_dir, file)
    img = nib.load(path)
    data = img.get_fdata()

    # Binarize: convert all tumor regions into 1
    data[data > 0] = 1

    # Save back, overwrite file
    new_img = nib.Nifti1Image(data, img.affine, img.header)
    nib.save(new_img, path)
    print("Binarized:", file)


